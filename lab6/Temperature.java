
public class Temperature{
  public static void main(String[] args) {

    double fahrenheit, celcius;

    celcius= 0.0;

    System.out.println("celcius -> fahrenheit");

    for (int i = 0;i <= 10 ;i++ ) {
      celcius = i *10;
      fahrenheit = ((celcius*9)/5) + 32;

      System.out.println(celcius + " -> " + fahrenheit);
    }
  }
}
